import streamlit as st
import pickle
import numpy as np

# -------------------------------------------------
# Load the trained model
# -------------------------------------------------
with open("loan_approval_model.pkl", "rb") as file:
    model = pickle.load(file)

# -------------------------------------------------
# Streamlit Page Config
# -------------------------------------------------
st.set_page_config(page_title="Loan Approval Prediction", layout="centered")

st.title("üè¶ Loan Approval Prediction System")
st.write("Enter applicant details below to predict loan approval status.")

# -------------------------------------------------
# User Inputs (MATCH DATASET FEATURES)
# -------------------------------------------------

no_of_dependents = st.number_input(
    "Number of Dependents", min_value=0, step=1
)

education = st.selectbox(
    "Education", ["Graduate", "Not Graduate"]
)

self_employed = st.selectbox(
    "Self Employed", ["Yes", "No"]
)

income_annum = st.number_input(
    "Annual Income", min_value=0
)

loan_amount = st.number_input(
    "Loan Amount", min_value=0
)

loan_term = st.number_input(
    "Loan Term (in months)", min_value=0
)

cibil_score = st.number_input(
    "CIBIL Score", min_value=300, max_value=900
)

residential_assets_value = st.number_input(
    "Residential Assets Value", min_value=0
)

commercial_assets_value = st.number_input(
    "Commercial Assets Value", min_value=0
)

luxury_assets_value = st.number_input(
    "Luxury Assets Value", min_value=0
)

bank_asset_value = st.number_input(
    "Bank Asset Value", min_value=0
)

# -------------------------------------------------
# Encode Categorical Variables (SAME AS TRAINING)
# -------------------------------------------------
education = 0 if education == "Graduate" else 1
self_employed = 0 if self_employed == "Yes" else 1

# -------------------------------------------------
# Prediction Button
# -------------------------------------------------
if st.button("Predict Loan Status"):

    # IMPORTANT: ORDER MUST MATCH TRAINING DATA
    input_data = np.array([[
        no_of_dependents,
        education,
        self_employed,
        income_annum,
        loan_amount,
        loan_term,
        cibil_score,
        residential_assets_value,
        commercial_assets_value,
        luxury_assets_value,
        bank_asset_value
    ]])

    prediction = model.predict(input_data)

    if prediction[0] == 0:
        st.success("‚úÖ Loan Approved")
    else:
        st.error("‚ùå Loan Rejected")